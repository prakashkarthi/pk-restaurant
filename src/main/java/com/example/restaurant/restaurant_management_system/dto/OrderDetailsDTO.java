package com.example.restaurant.restaurant_management_system.dto;

public class OrderDetailsDTO {
    private Long id;                // Unique identifier for the order detail
    private Long menuId;            // Reference to the menu item
    private String itemName;        // Name of the menu item
    private Integer quantity;       // Quantity of the item
    private Double price;           // Price of the item
    private Double totalPrice;      // Total price (quantity * price)

    // Getters and Setters
    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Long getMenuId() {
        return menuId;
    }

    public void setMenuId(Long menuId) {
        this.menuId = menuId;
    }

    public String getItemName() {
        return itemName;
    }

    public void setItemName(String itemName) {
        this.itemName = itemName;
    }

    public Integer getQuantity() {
        return quantity;
    }

    public void setQuantity(Integer quantity) {
        this.quantity = quantity;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public Double getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(Double totalPrice) {
        this.totalPrice = totalPrice;
    }
}
